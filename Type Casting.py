# String to Integer
num = int('123')

# List to Tuple
my_tuple = tuple([1, 2, 3])

# Tuple to List
my_list = list((4, 5, 6))

# List of pairs to Dictionary
pair_list = [(1, 'A'), (2, 'B')]
mapped_dict = dict(pair_list)

print(f"Int: {num}, Tuple: {my_tuple}, List: {my_list}, Dict: {mapped_dict}")
